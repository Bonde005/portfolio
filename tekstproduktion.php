        </section> <!-- annonce_overskrift -->

        <section id="annonce_tekst" class="tekstfelt">
          <div class="tekst">
            <p>En annonce skal være hurtig og præcis i sin kommunikation. Overskriften er her vigtigt da den skal skabe interesse og fange målgruppens opmærksomhed. Brødteksten i annoncen skal være kort og tydelig, hvor det basale kommer tydeligt frem. Her kan man med fordel benytte sig af AIDA modellen. Man skal først skabe opmærksomhed, derefter en interesse for produktet, efterfulgt af en længsel efter produktet som til sidst skal ende med an aktion hvor brugeren kan købe produktet. Attention fås i overskriften, hvor interessen og længslen fås i annoncens billeder og/eller brødtekst. Her skal budskabet være så klart at det skaber en interesse som bliver til en længsel. Aktion bliver frembragt af en call to action, som i dette tilfælde ofte er et firmanavn/logo eller hvor produktet kan købes. Teksten i annoncen kan blive udformet ved hjælp af afsæt i nogle strategier.</p>
          </div> <!-- tekst -->
          <div class="punkter">
            <dl>
              <dt>USP: Uniqe Selling Propostition</dt>
                <dd>Går på tilbud eller det unikke</dd>
              <dt>ESP: Emotional Selling Proposition</dt>
                <dd>Spiller på følelse, kan både være gode følelser og alvorlige.</dd>
              <dt>ISP: Ironic Selling Proposition</dt>
                <dd>Går på ironi og sort humor</dd>
            </dl>
          </div> <!-- punkterne -->
          <p class="kilde">(Kommunikation i multimediedesign, s. 244-246)</p>
