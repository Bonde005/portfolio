        <section id="funktioner_tekst" class="tekstfelt">
          <p>Der er nogle væsentlige funktioner i Illustrator, som er godt at kende til når man starter ud. Når man arbejder i Illustrator arbejder man i lag, så man nemt kan adskille og redigere i objekter. Derved får man også et bedre overblik over sit arbejde, især hvis man arbejder med noget kompleks der indgår en masse objekter, eller hvis man arbejder på mange forskellige produkter i samme fil. Det er nemlig muligt i Illustrator at arbejde på flere produkter i samme fil på en overskuelig måde. Det skyldes funktionen kaldet artboards. I sin fil hvor man arbejder, har man flere ”stykker papir”, hvor man kan arbejde derpå. Hvis man eksporter filen til f.eks. en pdf-fil, bliver det man har lavet på de individuelle artboards gemt på deres egne sider. De bliver altså ikke sat sammen til ét produkt. Derudover er det kun de selve objekter der befinder sig på artboardet og ikke dem udenfor. Det er ligesom at have et arbejdsbord med flere stykker papir på, hvor man kan lægge ting på papirerne og rykke dem ud på arbejdsbordet som man vil. Man kan altså godt flytte objekter væk fra artboardet i stedet for at slette dem, hvis man gerne vil gemme på dem. Udover disse to funktioner findes der mange flere i programmet som man kan beskæftige sig kendskab til.</p>
          <p class="kilde">(Carsten Kjeldsen Bogner, Illustrator: grundlæggende)</p>
        </section> <!-- funktioner_tekst -->

