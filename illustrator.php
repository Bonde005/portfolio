        <section id="indledning" class="tekstfelt">
          <p>Adobes program Illustrator bruges for det meste til logoer og ikoner. Programmet benytter sig af vektoren, som man kender dem fra matematikkens verden. Dvs. at der ikke anvendes pixels som i de fleste redigeringsprogrammer. Fordelen ved dette er at man kan skalere sit arbejde i Illustrator uden at der sker ændringer. Hvis man f.eks. gør et pixeleret arbejde meget større, bliver de individuelle pixels forstørret, og det medvirker et uklart produkt hvor man tydeligt kan se at det består af firkanter. Hvis man derimod forstørre et vektorprodukt undgår man dette problem. Derfor er Illustrator god til at designe logoer og ikoner, da de ofte skal bruges i forskellige størrelser til forskellige medier og andre produkter. Derved er der ingen problem ved skaleringen.</p>
          <p class="kilde">(Carsten Kjeldsen Bogner, Illustrator: grundlæggende)</p>
