          <h2>Faserne</h2>
        </section> <!-- faserne_overskrift -->

        <section id="faserne_tekst" class="tekstfelt">
          <p>Den første fase hedder empathize. Her gælder det om at være nysgerrigt, og opsøge så meget viden indenfor emnet som muligt. Ved at gøre dette kan man finde viden man slet ikke vidste der var der før. Her er det ikke kun research i form af tekster men også observationer og interviews. Den næste fase hedder define, her skal man til at kigge på alt den viden man har opnået, og herved finder et problem eller en vinkel. Hertil kan man lave en framing som man arbejder udefter efterfølgende. Det kan være at man i empathize fasen har fundet ud af at folk mister deres nøgler ofte, og dertil vil man define en vinkel/problem, hvor man vil hjælpe folk med at vide hvor deres nøgler er henne. Den næste fase er ideate, her skal man til at udføre en idégeneration. Her skal man komme med så mange idéer som muligt, og man må ikke sætte sig fast på en endnu. Der er ikke nogen dårlige idéer og skal derfor ikke binde sig til at være realistisk. Ved at tænke divergent får man ny inspiration. Først til sidst må man være konvergerende og sortere de urealistiske idéer fra. Her vælger man en idé at arbejde videre med. Næste fase er prototype. Her skal man til at bygge en prototype som man kan få testet. Den første prototype skal ikke være detaljeret, blot at man kan få videreført idéen. Til dette kan man benytte sig af papirprototyper, hvor man skitsere idéen. Næste fase er test. Ved at testet prototypen kan man undgå problemer og komme med nye idéer til at forbedre produktet, derved kan det være oplagt at gå tilbage til prototypefasen og lave en ny prototype som skal testes. Fasen herefter er implement. Når man er tilfreds med sit produkt og det fungerer ordentligt, eller hvis en deadline er tæt på, skal man til at implementere sin idé i virkeligheden. Dette kan være noget der skal implementeres til et eksisterende produkt, men kan også være at producere produktet og gør det tilgængeligt.</p>
          <p class="kilde">(Niels Østergaard, Intro til Design Thinking)</p>
        </section> <!-- faserne_tekst -->

